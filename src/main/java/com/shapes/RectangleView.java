package com.shapes;

public class RectangleView {
  public final double length;
  public final double width;
  public final double area;
  public final double perimeter;

  public RectangleView(final double length, final double width, final double area, final double perimeter) {
    this.length = length;
    this.width = width;
    this.area = area;
    this.perimeter = perimeter;
  }
}
